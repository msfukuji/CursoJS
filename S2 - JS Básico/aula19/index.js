/*
Tipos de dados primitivos:
*/

const nome = 'Marcelo'; // string
const nome1 = "Marcelo"; // string
const nome2 = `Marcelo`; // string
const num1 = 10; // number
const num2 = 10.52; // number
let nomeAluno; // undefined -> não aponta para nenhum local na memoória do JS. Variável declarada mas não inicializada
const sobrenomeAluno = null; // Nulo -> não aponta para nenhum local na memoória do JS.
const aprovado = true; // Valor Boolean (true or false)

console.log(typeof nome, nome);
console.log(typeof nome1, nome1);
console.log(typeof nome2, nome2);
console.log(typeof num1, num1);
console.log(typeof num2, num2);
console.log(typeof nomeAluno, nomeAluno);
console.log(typeof sobrenomeAluno, sobrenomeAluno);
console.log(typeof aprovado, aprovado);
